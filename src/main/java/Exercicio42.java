    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int numero = Integer.parseInt(jTextField1.getText());
        int i = 0;
        if(numero > 0){
           while(i <= numero){
            LabelActionWhile(i);
            i++;
        } 
        }
        else{
            JOptionPane.showMessageDialog(null, "Digite um número maior que 0");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
     
    public int posicaoY = 100;
    private void LabelActionWhile(int n){
        posicaoY += 20;
        JLabel lblWhile = new JLabel();
        lblWhile.setText("Valor do incremento: " + n);
        lblWhile.setVisible(true);
        lblWhile.setSize(150, 40);
        lblWhile.setLocation(70, posicaoY);
        getContentPane().add(lblWhile);
        this.revalidate();
        this.repaint();
    }
    
